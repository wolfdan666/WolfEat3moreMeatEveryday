void bubbleSort(vector<int>& a){
    // true和false 是优化的冒泡排序,因为如果已经有序了的话,那么将会直接返回了
    bool swapp = true;
    while(swapp){
        swapp = false;
        for (size_t i = 0; i < a.size()-1; i++) {
            if (a[i]>a[i+1] ){
                // swap(a[i],a[i+1])的不借助tmp的加减法版本,其实还可以用异或实现,当然也可以借助tmp,或者直接用标准库里面的swap()
                a[i] += a[i+1];
                a[i+1] = a[i] - a[i+1];
                a[i] -=a[i+1];
                swapp = true;
            }
        }
    }
}

